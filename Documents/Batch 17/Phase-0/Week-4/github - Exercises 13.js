function countProfit(shoppers) {
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

  // you can only write your code here!

  let dataAkhir = []
  let data = {}
  let dataPembeli = []
  let total = 0

  for (var i = 0; i < listBarang.length; i++) {

    data.product = listBarang[i][0]
    data.shoppers = []
    data.leftOver = listBarang[i][2]
    data.totalProfit = total

    dataAkhir.push(data)

    data = {}

  }

  for (var k = 0; k < dataAkhir.length; k++) {

    for (var l = 0; l < shoppers.length; l++) {

      if (dataAkhir[k].product === shoppers[l].product) {

        if (dataAkhir[k].leftOver >= shoppers[l].amount) {

          dataAkhir[k].shoppers.push(shoppers[l].name)
          dataAkhir[k].leftOver -= shoppers[l].amount

          // !!
          if (dataAkhir[k].shoppers.length !== 0) {

            // !!
            dataAkhir[k].totalProfit = (listBarang[k][2] - dataAkhir[k].leftOver) * listBarang[k][1]

          }

        }

      }

    }

  }


return dataAkhir

}

